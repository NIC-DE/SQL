SELECT "ΚωδικόςΠολίτη",count("ΗμερομηνίαΕισόδου")
AS "ΠλήθοςΕπισκέψεωνΣεΚαταστήματα"
FROM   "ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ"
WHERE  "ΗμερομηνίαΕισόδου" BETWEEN 15 AND 28 
AND ( "ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ"."ΚωδικόςΠολίτη" )IN
         (SELECT "ΠΟΛΙΤΗΣ"."ΚωδικόςΠολίτη"
          FROM  "ΠΟΛΙΤΗΣ"
          WHERE "ΚωδικόςΚατοικίας"="Κατοικία01"	)	     
		  GROUP BY "ΚωδικόςΠολίτη" 
		  ORDER BY "ΠλήθοςΕπισκέψεωνΣεΚαταστήματα" ;
       
        

   SELECT DISTINCT "ΚωδικόςΚαταστήματος","ΛεπτόΕισόδου","ΔιάρκειαΠαραμονής"
   FROM  "ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ"
   WHERE ("ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ"."ΚωδικόςΠολίτη")IN
             (SELECT "ΠΟΛΙΤΗΣ"."ΚωδικόςΠολίτη"
              FROM  "ΠΟΛΙΤΗΣ"
              WHERE "Πολίτης12"="ΚωδικόςΠολίτη" AND "ΚωδικόςΠολίτη" IS NOT NULL
              AND "Covid19Από"-1="ΗμερομηνίαΕισόδου" 
              GROUP BY "ΚωδικόςΚαταστήματος"
               ); 

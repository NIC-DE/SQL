SELECT "Τύπος" AS "ΤύποςΚαταστήματος",
count("ΗμερομηνίαΕισόδου") AS "ΠλήθοςΕπισκέψεωνΠολιτών",
round(avg("ΔιάρκειαΠαραμονής"),1)
AS "ΜέσηΔιάρκειαΠαραμονήςΠολιτών"
FROM "ΚΑΤΑΣΤΗΜΑ"
INNER JOIN "ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ" ON
"ΚΑΤΑΣΤΗΜΑ"."ΚωδικόςΚαταστήματος"="ΕΠΙΣΚΕΨΗΠΟΛΙΤΗ"."ΚωδικόςΚαταστήματος"
GROUP BY "Τύπος"
ORDER BY "Τύπος" ASC;
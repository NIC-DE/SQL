ALTER TABLE "ΝΟΣΗΜΑΠΟΛΙΤΗ"
ADD "ΥπόΔιερεύνηση" INTEGER;
UPDATE "ΝΟΣΗΜΑΠΟΛΙΤΗ"
SET "ΥπόΔιερεύνηση"=0
WHERE "ΥποκείμενοΝόσημα" IS NOT NULL;
UPDATE "ΝΟΣΗΜΑΠΟΛΙΤΗ"
SET "ΥπόΔιερεύνηση"=1
WHERE "ΥποκείμενοΝόσημα" IN(SELECT "ΥποκείμενοΝόσημα"
FROM "ΝΟΣΗΜΑΠΟΛΙΤΗ"
WHERE "ΥποκείμενοΝόσημα"="νευρολογικό"
OR "ΥποκείμενοΝόσημα"="νευρομυϊκο" );